/*  
 * 문제 요약
 *     - 자연수 n을 x로 나눴을 때 나머지가 1이 되는 가장 작은 자연수 x를 구하는 문제.
 *
 * 입력  
 *     - 정수 n (3 ≤ n ≤ 1,000,000)
 *
 * 출력  
 *     - n % x == 1을 만족하는 가장 작은 자연수 x
 *
 * 핵심 포인트  
 *     - x는 항상 존재하므로, 2부터 n-1까지 순차적으로 나누며 첫 번째로 나머지가 1이 되는 값을 반환
 */

class Solution {
	
    public int solution(int n) {
        // 2부터 n-1까지 순회하며 나머지가 1이 되는 첫 번째 수를 찾음
        return IntStream.range(2, n)
                        .filter(i -> n % i == 1)  // 나머지가 1인지 확인
                        .findFirst()             // 가장 먼저 나오는 수 반환
                        .orElse(-1);             // 이론상 항상 존재하므로 -1은 반환되지 않음
    }
}
