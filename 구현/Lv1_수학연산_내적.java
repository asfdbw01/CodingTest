/*  
 * 문제 요약  
 *     - 두 정수 배열 a, b가 주어졌을 때, 각 인덱스에 해당하는 값들의 곱을 모두 더한 값을 반환  
 *     - 즉, 내적: a[0]*b[0] + a[1]*b[1] + ... + a[n-1]*b[n-1] 계산  
 *
 * 입력  
 *     - int[] a: 길이 1 이상 1,000 이하의 정수 배열  
 *     - int[] b: 길이 1 이상 1,000 이하의 정수 배열  
 *
 * 출력  
 *     - int: 두 배열의 내적 결과  
 *
 * 핵심 포인트  
 *     - Stream을 이용하여 인덱스를 순회하며 각 곱을 더하는 방식으로 간결하게 구현  
 *     - 배열의 길이가 동일하다는 보장 덕분에 별도 유효성 검사는 불필요  
 */

class Solution {
	
	public int solution(int[] a, int[] b) {
		// 인덱스를 0부터 a.length-1까지 순회하면서 a[i] * b[i]의 총합을 구함
		return IntStream.range(0, a.length).map(i -> a[i] * b[i]).sum();
	}
	
}
