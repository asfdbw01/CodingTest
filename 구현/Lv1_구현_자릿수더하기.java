/*  
 * 문제 요약
 *     - 자연수 N의 각 자릿수를 모두 더한 값을 반환  
 *
 * 입력  
 *     - 자연수 N (1 이상 100,000,000 이하)  
 *
 * 출력  
 *     - 자릿수의 합 (정수)  
 *
 * 핵심 포인트  
 *     - 숫자를 문자열로 변환하여 각 자리의 문자를 숫자로 변환  
 *     - Stream을 통해 간결하게 자릿수 합산 처리  
 */

class Solution {
	
	public int solution(int n) {
		// 숫자를 문자열로 바꾸고 각 자릿수를 문자로 처리한 후,
		// '0'을 빼서 숫자로 변환한 뒤 전부 더함
		return String.valueOf(n)
					 .chars()
					 .map(c -> c - '0')
					 .sum();
	}
	
}
