/*  
 * 문제 요약
 *     - 주어진 정수 배열을 이어붙여 만들 수 있는 가장 큰 수를 문자열로 반환하는 문제
 *     - 배열의 숫자들을 조합하여 사전순으로 가장 큰 수가 되도록 정렬한 후 문자열로 연결
 *
 * 입력  
 *     - numbers: int[] (길이 1 이상 100,000 이하, 각 원소는 0 이상 1,000 이하의 정수)
 *
 * 출력  
 *     - 가장 큰 수를 표현한 문자열 (앞자리에 불필요한 0 제거 포함)
 *
 * 핵심 포인트  
 *     - 숫자를 문자열로 변환한 뒤, (b + a)와 (a + b)를 비교해 정렬 순서를 결정  
 *     - 정렬된 문자열을 모두 이어붙이면 가장 큰 수가 됨  
 *     - "000..."처럼 모든 숫자가 0일 경우를 처리하기 위해 앞의 0을 제거
 */

class Solution {
	
	public String solution(int[] numbers) {
		return Arrays.stream(numbers)
				.mapToObj(String::valueOf)						// 각 정수를 문자열로 변환
				.sorted((a, b) -> (b + a).compareTo(a + b))		// 두 수를 이어붙인 결과를 비교하여 내림차순 정렬
				.collect(Collectors.joining())					// 정렬된 문자열들을 하나로 합침
				.replaceFirst("^0+(?!$)", "");					// 전체가 0인 경우를 처리하기 위해 앞의 연속된 0을 하나로 축약
	}
	
}
