/*  
 * 문제 요약
 *     - 두 정수 a, b 사이의 모든 정수의 합을 구함 (a와 b 포함)
 *     - a와 b의 대소관계는 정해져 있지 않음
 *
 * 입력  
 *     - int a, b: -10,000,000 이상 10,000,000 이하
 *
 * 출력  
 *     - long: a와 b 사이 모든 정수의 합
 *
 * 핵심 포인트  
 *     - 반복문 없이 등차수열의 합 공식을 활용하여 효율적으로 계산
 *     - 중간 결과가 실수이므로 (a + b) / 2.0 후 곱셈 → long 캐스팅
 *     - 절댓값 대신 (큰 값 - 작은 값 + 1)로 범위 길이 구함
 */

class Solution {
	
	public long solution(int a, int b) {
		// 등차수열 합: (첫항 + 끝항) * 항 개수 / 2
		return (long) ((a + b) / 2.0 * ((a > b) ? a - b + 1 : b - a + 1));
	}
	
}
