/*  
 * 문제 요약  
 *     - 특정 거리 N까지 이동할 때, 순간이동은 건전지를 소모하지 않고  
 *       앞으로 K칸 점프는 K만큼 건전지를 소모함 (여기서 K = 1)  
 *     - 순간이동을 최대한 활용하고, 점프 횟수를 최소화할 때  
 *       필요한 건전지 사용량(= 점프 횟수)의 최솟값을 구하는 문제  
 *
 * 입력  
 *     - n: 이동하려는 거리 (1 이상 10억 이하의 자연수)  
 *
 * 출력  
 *     - 최소 건전지 사용량 (즉, 최소 점프 횟수)  
 *
 * 핵심 포인트  
 *     - 순간이동은 짝수일 때만 가능하므로, 홀수일 때는 반드시 점프가 필요함  
 *     - n이 홀수일 때만 1을 빼고, 짝수일 때는 /2 반복  
 */

class Solution {
	
	public int solution(int n) {
		int power = 0;
		
		while (n > 0) {
			if (n % 2 == 0) n /= 2;   // 짝수면 순간이동 (건전지 소모 X)
			else {
				n--;                 // 홀수면 점프 (건전지 소모 1)
				power++;
			}
		}
		
		return power;
	}
	
}
